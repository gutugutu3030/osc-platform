package com.oscplatform.adapter.mcp

/**
 * MCPホスト側のクエリルーター。
 *
 * ユーザー入力に含まれるキーワードに基づいて、時間依存・記憶依存の判定を行い、
 * 必要なツール種別を返す。LLMの暗黙知に頼らず、時間・最新性・記憶の判断を
 * ホスト側で明示的に制御するために使用する。
 */
object McpQueryRouter {

    /** 時間依存・最新性依存のキーワード。
     *
     * 「今」（漢字）と「いま」（ひらがな）は両方の表記でユーザーが入力しうるため、
     * 重複に見えるが意図的に両方含めている。
     * substring マッチのため「今後」「今回」などにも反応しうるが、
     * ルーティング方針として時間依存の判定は積極的（false negative より false positive を許容）。
     */
    val timeKeywords: Set<String> = setOf(
        "最近", "最新", "今", "現在", "直近", "さっき", "先ほど", "いま",
        "今日", "昨日", "今週", "先週", "直近数日", "この前",
    )

    /** 記憶・履歴依存のキーワード */
    val memoryKeywords: Set<String> = setOf(
        "思い出", "直近の会話", "以前の出来事", "これまでの履歴",
        "写真", "過去の行動", "過去の記録", "履歴",
    )

    /**
     * クエリを分類し、必要なツール種別を返す。
     *
     * @param query ユーザーの入力テキスト
     * @return 必要なツール呼び出し種別を示す [QueryRouteDecision]
     */
    fun classify(query: String): QueryRouteDecision {
        val needsTime = timeKeywords.any { query.contains(it) }
        val needsMemory = memoryKeywords.any { query.contains(it) }
        return QueryRouteDecision(needsCurrentTime = needsTime, needsMemorySearch = needsMemory)
    }

    /** ルーティング判定結果 */
    data class QueryRouteDecision(
        /** 現在時刻の取得が必要か */
        val needsCurrentTime: Boolean,
        /** 記憶・履歴の検索が必要か */
        val needsMemorySearch: Boolean,
    ) {
        /** 何らかのツール呼び出しが必要か */
        val requiresToolCall: Boolean get() = needsCurrentTime || needsMemorySearch
    }

    /** ツール強制ルーターのプロンプト名 */
    const val PROMPT_NAME: String = "tool_force_router"

    /** ツール強制ルーターのプロンプト説明 */
    const val PROMPT_DESCRIPTION: String =
        "時間・最新性・記憶に関するクエリを適切なMCPツールへルーティングするシステムプロンプト"

    /**
     * ツール強制ルーターのシステムプロンプト。
     *
     * MCPクライアント（LLM）がこのプロンプトを取得することで、
     * 時間依存・記憶依存のクエリに対して必ずツールを使うよう指示される。
     */
    val systemPromptText: String = """
あなたはMCPホスト側のルーターである。
ユーザー入力を受け取り、必要に応じて適切なMCPツール呼び出しを行う。
重要なのは、LLMの暗黙知に頼らず、時間・最新性・記憶の判断をホスト側で明示的に制御すること。

# 目的
- 「最近」「最新」「今」などの曖昧な時間表現を含む問い合わせを、誤った固定時点の知識で答えないようにする
- 時間依存の質問は、必ず現在時刻または最新情報を参照する
- 記憶系の質問は、ローカルの記憶/履歴/MCPサーバの記憶検索へルーティングする
- ユーザーの自然文をできるだけ保ちながら、必要なツールだけを呼ぶ

# 判定対象
次の語や意図を含む入力は、時間依存または最新性依存として扱う。
- 最近, 最新, 今, 現在, 直近, 直近の, さっき, 先ほど, いま, 今日, 昨日, 今週, 先週, 直近数日, この前
- 「今どうなっているか」「最近の状況」「最新の結果」「直近の変更」「現在の値」など

次の意図を含む入力は、記憶/履歴依存として扱う。
- 最近の思い出
- 直近の会話
- 以前の出来事
- これまでの履歴
- いつの写真/出来事か知りたい
- 過去の行動や記録を探したい

# ルーティング方針
1. 時間表現や最新性が少しでも関係する場合、現在時刻を取得する。
2. 最新情報が必要な場合は、web検索または対応する検索ツールを必ず使う。
3. 「最近の思い出」「過去の記録」「写真検索」などは、記憶系MCPツールに振る。
4. 1回の問い合わせで複数意図がある場合は、必要なツールを順番に組み合わせる。
5. ツールを呼ばずに推測で答えない。
6. 曖昧な時間表現は、ユーザーの意図を勝手に補完しすぎず、まずは時間基準を明示して扱う。

# 実行ルール
- 「最近」= 過去7日を基本候補として扱う
- 「直近」= 過去24時間〜7日を文脈で調整する
- 「今日」「昨日」「今週」などは絶対日時に変換して扱う
- 最新性が重要な問い合わせでは、時刻取得だけで終わらず、必要なら検索も行う
- 回答時には、参照した時刻や対象期間がわかるようにする

# ツール選択の原則
- 時間の基準が必要 -> current time 系ツール
- 最新情報が必要 -> web search 系ツール
- 履歴/記憶が必要 -> memory / recent memories 系ツール
- 条件が複数なら順番に呼ぶ
  例:
  - 現在時刻を取得
  - 期間を確定
  - 対象を検索
  - 結果を整形して返す

# 禁止事項
- モデルの学習時点の知識だけで「最近」「最新」に答えない
- 時間を含む質問を見逃して、通常回答として処理しない
- ツール呼び出しなしで「たぶん」「おそらく」で済ませない
- 記憶系の質問を web 検索だけで代用しない

# 望ましい出力
- 必要なら、まずツールを呼ぶ
- 最終回答では、対象期間や基準時刻を短く明示する
- 余計な説明は増やしすぎず、ユーザーの質問に直接答える
    """.trimIndent()
}
